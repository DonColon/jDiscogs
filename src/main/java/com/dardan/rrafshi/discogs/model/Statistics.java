package com.dardan.rrafshi.discogs.model;

public final class Statistics
{
	private long releases;

	private long artists;

	private long labels;


	public long getReleases()
	{
		return this.releases;
	}

	public long getArtists()
	{
		return this.artists;
	}

	public long getLabels()
	{
		return this.labels;
	}
}
